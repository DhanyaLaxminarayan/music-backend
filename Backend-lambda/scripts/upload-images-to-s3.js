/**
 * Download unique song images from 2026a2_songs.json and upload them to S3.
 */

import { readFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { PutObjectCommand, S3Client } from '@aws-sdk/client-s3';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const songsFilePath = path.resolve(__dirname, '../2026a2_songs.json');

const region = process.env.AWS_REGION || 'us-east-1';
const bucket = process.env.S3_BUCKET;

if (!bucket) {
  console.error('Missing S3_BUCKET environment variable.');
  process.exit(1);
}

const s3Client = new S3Client({ region });

function slugify(value) {
  return value
    .toString()
    .trim()
    .toLowerCase()
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

async function loadSongs() {
  const raw = await readFile(songsFilePath, 'utf8');
  const data = JSON.parse(raw);

  if (!data || !Array.isArray(data.songs)) {
    throw new Error('Invalid 2026a2_songs.json format: expected { songs: [...] }');
  }

  return data.songs;
}

function buildUploadPlan(songs) {
  const uploadsByUrl = new Map();
  const keyToImageUrl = new Map();

  for (const song of songs) {
    if (!song || typeof song !== 'object') {
      throw new Error('Invalid song entry found in 2026a2_songs.json');
    }

    const artist = String(song.artist || '').trim();
    const imgUrl = String(song.img_url || '').trim();

    if (!artist || !imgUrl) {
      throw new Error(`Missing artist or img_url for song: ${JSON.stringify(song)}`);
    }

    if (uploadsByUrl.has(imgUrl)) {
      continue;
    }

    const key = `artist-images/${slugify(artist)}.jpg`;

    if (keyToImageUrl.has(key)) {
      throw new Error(`S3 key collision detected: ${key} generated by multiple img_url values`);
    }

    uploadsByUrl.set(imgUrl, { imgUrl, artist, key });
    keyToImageUrl.set(key, imgUrl);
  }

  return Array.from(uploadsByUrl.values());
}

async function downloadImage(imgUrl) {
  const response = await fetch(imgUrl);

  if (!response.ok) {
    throw new Error(`Failed to download image: ${imgUrl} (${response.status} ${response.statusText})`);
  }

  const contentType = response.headers.get('content-type') || 'image/jpeg';
  const body = Buffer.from(await response.arrayBuffer());

  return { contentType, body };
}

async function uploadImages() {
  const songs = await loadSongs();
  const uploadPlan = buildUploadPlan(songs);

  console.log(`Uploading ${uploadPlan.length} unique img_url images to s3://${bucket}/ in ${region}...\n`);

  for (const item of uploadPlan) {
    const { contentType, body } = await downloadImage(item.imgUrl);

    await s3Client.send(
      new PutObjectCommand({
        Bucket: bucket,
        Key: item.key,
        Body: body,
        ContentType: contentType
      })
    );

    console.log(`Uploaded ${item.artist} -> ${item.key}`);
  }

  console.log(`\nCompleted image upload to bucket: ${bucket}`);
  console.log('Use the generated S3 object keys or URLs in the music table image_url field.');
}

uploadImages().catch(error => {
  console.error('Error uploading images:', error);
  process.exit(1);
});
